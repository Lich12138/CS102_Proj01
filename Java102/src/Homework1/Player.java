package Homework1;

public class Player {
	private int score;
	private int loc;
	
	public Player() {
		setScore(0);
		setLoc(0);
	}

	public int getScore() {
		return score;
	}

	public void setScore(int score) {
		this.score = score;
	}

	public int getLoc() {
		return loc;
	}

	public void setLoc(int loc) {
		this.loc = loc;
	}

	
}
